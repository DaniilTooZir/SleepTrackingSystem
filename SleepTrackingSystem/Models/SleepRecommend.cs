        public static List<string> GetRecommendations(double averageQuality, double averageDuration)
        {
            if (averageQuality == 5 && averageDuration > 8)
                return recommendations["Excellent"];
            else if (averageQuality >= 4 && averageQuality < 5 && averageDuration > 6)
                return recommendations["Good"];
            else if (averageQuality >= 3 && averageQuality < 4 && averageDuration > 4)
                return recommendations["Average"];
            else if (averageQuality >= 1.5 && averageQuality < 3 && averageDuration < 3)
                return recommendations["Poor"];
            else
                return recommendations["Terrible"];

